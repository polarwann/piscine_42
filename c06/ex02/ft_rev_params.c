#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	y;

	i = (argc - 1);
	while (i > 0)
	{
		y = 0;
		while (argv[i][y])
			y++;
		write(1, argv[i], y);
		write(1, "\n", 1);
		i--;
	}
}
