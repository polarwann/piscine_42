#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	y;

	i = 1;
	while (i < argc)
	{
		y = 0;
		while (argv[i][y])
			y++;
		write(1, argv[i], y);
		write(1, "\n", 1);
		i++;
	}
}
