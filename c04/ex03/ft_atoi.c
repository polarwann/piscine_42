int	ft_atoi(char *str);

int	ft_atoi(char *str)
{
	int	i;
	int	y;
	int	s;

	y = 1;
	i = 0;
	s = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			y *= (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (str[i] != '\0')
			s = s * 10 + (str[i] - '0');
		i++;
	}
	if (y == (-1))
		return (s * y);
	return (s);
}
