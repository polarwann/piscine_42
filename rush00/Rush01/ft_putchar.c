#include <unistd.h>

void	ft_putchar(char f, int n)
{
	if (f == 'A')
		write(1, "/", 1);
	if (f == 'C')
		write(1, "\\", 1);
	while (n-- > 2)
	{
		if (f == 'B')
			write(1, "*", 1);
		if (f == 'S')
			write(1, " ", 1);
	}
	if (f == 'B' && n < 1)
		write (1, "*", 1);
	if (f == 'n')
		write(1, "\n", 1);
}
