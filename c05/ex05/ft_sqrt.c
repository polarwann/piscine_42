int	ft_sqrt(int nb);

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (i < 50000)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
